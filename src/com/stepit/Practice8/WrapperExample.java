package com.stepit.Practice8;

public class WrapperExample {

    public static void main(String[] args) {
        Byte byteExample = 127;
        Short shortExample = 3000;
        Integer intExample = 300000;
        Long longExample = 300000L;
        Float floatExample = 0.3f;
        Double doubleExample = 0.3;
        Boolean booleanExample = true;
        String charExample = "a";
        System.out.println(byteExample);
        System.out.println(intExample.intValue());
        System.out.println(floatExample);
    }
}
